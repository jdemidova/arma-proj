% Система уравнений для модели АРCC(2,1):
function f = ARMA21(x)
  R0 = 480.2416534154067;
  R1 = 194.8616812096158;
  R2 = -150.93877797958476;
  R3 = -274.6778639330756;
  a0 = x(1);
  a1 = x(2);
  b1 = x(3);
  b2 = x(4);
  R_0 = a0;
  R_1 = b1*a0+a1;
  f(1) = -R0 + b1*R1 + b2*R2 + a0*R_0 + a1*R_1;
  f(2) = -R1 + b1*R0 + b2*R1 + a1*R_0;
  f(3) = -R2 + b1*R1 + b2*R0;
  f(4) = -R3 + b1*R2 + b2*R1;
end

